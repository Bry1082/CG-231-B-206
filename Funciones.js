



 //Funciones de Three,js
function EscaladoReal(obj,Sx,Sy,Sz){
   obj.scale(Sx,Sy,Sz);

}
function Rotaciony(obj,Ry){
   
    obj.rotateY(Ry);
    
 
 }
 function Rotacionx(obj,Rx){
   
   
    obj.rotateX(Rx);
    
 
 }
 function Rotacionz(obj,Rz){
   
   
    obj.rotateZ(Rz);
 
 }
 function TranslacionReal(obj,Tx,Ty,Tz){
    obj.translate(Tx,Ty,Tz);
 
 }